    private ArrayList<String> tratarNumSemanas()
    {
        if(ComboBoxAno.getSelectedItem().toString().equals("Ano"))
        {
            return new ArrayList<String>();
        }
        int ano = Integer.parseInt(ComboBoxAno.getSelectedItem().toString());
        String data = "";
           
        
        int diaSemana = diaDaSemana(ano-1, 13,1);
        
        LocalDate date = LocalDate.of(ano, 6, 1);
        int numSemanasAno = (int) IsoFields.WEEK_OF_WEEK_BASED_YEAR.rangeRefinedBy(date).getMaximum();
        
        
        int numDias = 0;
        
        if(diaSemana == 1)
            numDias = 6;
        else if(diaSemana == 0)
            numDias = 5;
        else if(diaSemana > 1 && diaSemana <= 6 )
            numDias = diaSemana - 2;
        
        ArrayList<String> primeirosDiasSemana = new ArrayList<>();
        int diaAtual = 0;
        int lim = 31;
        int mes = 12;
        switch(numDias)
        {
            case 0:
                data = "01-01";  
                diaAtual = 1;
                mes = 1;
                break;
            case 1:
                //Terça
                data ="12-31";
                diaAtual = 31;
                break;
            case 2:
                //Quarta
                data ="12-30";
                diaAtual = 30;
                break;
            case 3:
                //Quinta
                data ="12-29";
                diaAtual = 29;
                break;
            case 4:
                //Sexta
                data ="12-28";
                diaAtual = 28;
                break;
            case 5:
                //Sábado
                data ="12-27";
                diaAtual = 27;
                break;
            case 6:
                //Domingo
                data ="12-26";
                diaAtual = 26;
                break;
        }
        
        primeirosDiasSemana.add(data);
        for(int i = 0 ; i < numSemanasAno-1 ; i++)
        {
            if((diaAtual + 7) > lim )
            {
                diaAtual = (diaAtual + 7) - lim;
                mes++;
                
                switch(mes)
                {
                    case 1:
                    case 3:
                    case 5:
                    case 7:
                    case 8:
                    case 10:
                    case 12:
                        lim = 31;
                        break;
                    
                    case 4:
                    case 6:
                    case 9:
                    case 11:
                        lim = 30;
                        break;
                    
                    case 13:
                        mes = 1;
                        lim = 31;
                        break;
                    
                    case 2:
                        if((ano & 3) == 0 && ((ano % 25) != 0 || (ano & 15) == 0))

                            lim = 29;
                        else 
                            lim = 28;
                        break;
                
                }
                if(mes < 10)
                    data = "0"+mes+"-";
                else 
                    data = ""+mes+"-";
                
                if(diaAtual < 10)
                    data +="0"+diaAtual;
                else
                    data +=diaAtual;
            }
            else
            {
                diaAtual += 7;
                
                if(mes < 10)
                    data = "0"+mes+"-";
                else 
                    data = ""+mes+"-";
                
                if(diaAtual < 10)
                    data +="0"+diaAtual;
                else
                    data +=diaAtual;
            }
            primeirosDiasSemana.add(data);
        }

        return primeirosDiasSemana;
    }
    
    
    
